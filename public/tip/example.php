 <span class="style1">DR. NACCARATO'S WEEKLY PRACTICE-BUILDING COACHING MESSAGE </span><br> 
 <span class="style3">Keeping You Connected</span><br> 


 <span class="style6"><strong>"Burnout"</strong></span> 
 <br>

 <div align="left"><span align="left" class="style7">It is common for doctors to talk to me about burnout: the deep-seated sense of weariness and exhaustion they struggle to overcome but never do. They feel the burnout is because of the profession they are in and the challenges they are experiencing. Not so. Let me explain: <br>
<br>
I had a nice visit with a successful lawyer. I asked him how he enjoyed his work. He told me he was &ldquo;burnt-out&rdquo; and that if he could, he would change jobs in a minute. He was tired of the fight, emotionally spent, and ready to do something different. This good lawyer was taken back when I told him I see chiropractors, medical doctors, dentists, professors, etc. go through the same thing...especially as they get into their forties and fifties. (Once they get into their sixties they seem to simply buckle down, put these feelings aside, and hang in there until they retire.) <br>
<br>
<strong>Truth:</strong> Burnout is not a reflection of a person&rsquo;s profession; rather it is the result of battling the battle for a long time. It is the result of allowing one&rsquo;s heart to change from going to work fully committed to finding and serving patients, to going to work and focusing on oneself&hellip;specifically resolving personal concerns and fulfilling personal wants, desires, and needs. <br>
<br>
<strong>Bottom Line</strong>: Resolving personal concerns and achieving personal wants, desires, and needs is not a bad thing. It is a good thing. In fact, it is essential for good physical, emotional, and spiritual health. However, work is not the place to focus on these things. Why? You can&rsquo;t focus on yourself, your family, your problems, or your &ldquo;whatever&rdquo; while focusing on  finding and serving patients. If you do, burnout will not be an if, but rather a when. You will burnout! <br>
<br>
<strong>Application</strong>: You are a doctor. When you are at work you&rsquo;re on the &ldquo;clock,&rdquo; and it is not your time. It is your current and prospective patients&rsquo; time. Therefore, when you walk through the front of your office you need to leave your &ldquo;stuff&rdquo; outside the office and discipline yourself in the manner needed to find and serve patients. By giving yourself completely to the good of others, not only will you avoid burnout, you will be energized. You will attract more patients to your practice than you dreamed possible, your patients will stay under your care even when insurance companies refuse to pay, your income will increase, and your stress level will reduce considerably. My suggestion&hellip; try it for a week. Get to the office early; write down your personal concerns, wants, desires, and needs on a sheet of paper. Put that paper in an envelope, walk out of the office,  and put the envelop in your car. Return to the office and focus all of your time, talent, and energy on finding and serving patients. (Get your staff to do the same.) Then when personal concerns, wants, desires, and needs come to your or your staff&rsquo;s mind, remember that you wrote them down on a sheet of paper, you left the paper in your car, you are now at work, and you will deal with them during lunch or after work. Then, get back to finding and serving patients and watch a practice-building miracle happen. Think about it! Do something about it&hellip; today! <br>
